#include "libft.h"

int	ft_isalnum(int i)
{
	if ((i >= 97 && i <= 122) || (i >= 65 && i <= 90) || (i >= 48 && i <= 57))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
